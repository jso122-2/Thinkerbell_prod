# Main function
main() {
    local command=${1:-sync}
    
    case $command in
        "sync")
            run_sync
            ;;
        "help"|*)
            echo "Usage: $0 [sync|help]"
            echo ""
            echo "Commands:"
            echo "  sync    - Sync files from Windows to Ubuntu"
            echo "  help    - Show this help message"
            ;;
    esac
}

